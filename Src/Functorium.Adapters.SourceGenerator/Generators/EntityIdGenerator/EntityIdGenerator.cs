/// <summary>
/// [GenerateEntityId] 속성이 붙은 Entity 클래스에 대해
/// EntityId, EntityIdComparer, EntityIdConverter를 자동 생성하는 소스 생성기.
/// </summary>
[Generator(LanguageNames.CSharp)]
