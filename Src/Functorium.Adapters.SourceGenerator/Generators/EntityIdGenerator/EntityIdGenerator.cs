    private const string AttributeName = "GenerateEntityId";
    private const string AttributeNamespace = "Functorium.Domains.SourceGenerator";
    private const string FullyQualifiedAttributeName = $"{AttributeNamespace}.{AttributeName}Attribute";
    private const string GenerateEntityIdAttributeFileName = "GenerateEntityIdAttribute.g.cs";

    public const string GenerateEntityIdAttribute = Header + """

        namespace Functorium.Domains.SourceGenerator;

        /// <summary>
        /// Entity 클래스에 이 속성을 적용하면 해당 Entity의 ID 타입이 자동으로 생성됩니다.
        /// 생성되는 타입: {EntityName}Id, {EntityName}IdComparer, {EntityName}IdConverter
        /// </summary>
        [global::System.AttributeUsage(global::System.AttributeTargets.Class, AllowMultiple = false, Inherited = false)]
        [global::System.Diagnostics.CodeAnalysis.ExcludeFromCodeCoverage(Justification = "Generated by source generator.")]
        public class GenerateEntityIdAttribute : global::System.Attribute;
        """;

    private static IncrementalValuesProvider<EntityIdInfo> RegisterSourceProvider(
        IncrementalGeneratorInitializationContext context)
    {
        // 1단계: 고정 코드 생성 (Post-initialization)
        //  - GenerateEntityIdAttribute 정의
        context.RegisterPostInitializationOutput(ctx =>
            ctx.AddSource(
                hintName: GenerateEntityIdAttributeFileName,
                sourceText: SourceText.From(GenerateEntityIdAttribute, Encoding.UTF8)));

        // 2단계: [GenerateEntityId] 속성이 붙은 클래스 필터링
        return context
            .SyntaxProvider
            .ForAttributeWithMetadataName(
                fullyQualifiedMetadataName: FullyQualifiedAttributeName,
                predicate: IsClass,
                transform: MapToEntityIdInfo)
            .Where(x => x != EntityIdInfo.None);
    }

    private static EntityIdInfo MapToEntityIdInfo(GeneratorAttributeSyntaxContext context, CancellationToken cancellationToken)
    {
        if (context.TargetSymbol is not INamedTypeSymbol classSymbol)
        {
            return EntityIdInfo.None;
        }

        cancellationToken.ThrowIfCancellationRequested();

        string entityName = classSymbol.Name;
        string @namespace = classSymbol.ContainingNamespace.IsGlobalNamespace
            ? string.Empty
            : classSymbol.ContainingNamespace.ToString();

        string idTypeName = $"{entityName}Id";
        Location? location = context.TargetNode.GetLocation();

        return new EntityIdInfo(@namespace, entityName, idTypeName, location);
    }

    private static void Generate(SourceProductionContext context, ImmutableArray<EntityIdInfo> entityIdInfos)
    {
        foreach (var info in entityIdInfos)
        {
            StringBuilder sb = new();
            string source = GenerateEntityIdSource(info, sb);

            // 네임스페이스의 마지막 부분 추출
            string namespaceSuffix = string.Empty;
            if (!string.IsNullOrEmpty(info.Namespace))
            {
                var lastDotIndex = info.Namespace.LastIndexOf('.');
                if (lastDotIndex >= 0)
                {
                    namespaceSuffix = info.Namespace.Substring(lastDotIndex + 1) + ".";
                }
            }

            context.AddSource(
                $"{namespaceSuffix}{info.IdTypeName}.g.cs",
                SourceText.From(source, Encoding.UTF8));
        }
    }

    private static string GenerateEntityIdSource(EntityIdInfo info, StringBuilder sb)
    {
        sb.Append(Header)
            .AppendLine()
            .AppendLine("using System.ComponentModel;")
            .AppendLine("using System.Diagnostics;")
            .AppendLine("using System.Text.Json;")
            .AppendLine("using System.Text.Json.Serialization;")
            .AppendLine()
            .AppendLine($"namespace {info.Namespace};")
            .AppendLine();

        // EntityId 구조체 생성
        GenerateEntityIdStruct(sb, info);

        // EntityIdComparer 생성
        GenerateEntityIdComparer(sb, info);

        // EntityIdConverter 생성
        GenerateEntityIdConverter(sb, info);

        return sb.ToString();
    }

